package Introduccion_Java;

public class Variables {

    public static void main(String[] args) {
        // Tipos de Variavles en Java: Númericos, Texto y Booleanos.
        
        //Variables de Tipo Numérios.
        int entero = 5;
        double decimal = 13.2;
        float flotante = 7.8f;
        
        System.out.println("Numero Entero: " + entero);
        System.out.println("Numero Decimal: " + decimal);
        System.out.println("Numero Entero: " + flotante);
        
        //Variables de Tipo Texto.
        String texto = "Programacion con Cachi";
        char caracter = 'A';
        
        System.out.println("\nLa Cadena de Texto es: " + texto);
        System.out.println("El Caracter es: " + caracter);
        
        //Variables Boolenas.
        
        boolean verdadero = true;
        boolean falso = false;
        
        System.out.println("\nEs Verdadero: " + verdadero);
        System.out.println("Es falso: " + falso);
    }
}

